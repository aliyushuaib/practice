/*logical operators
in logical operators we have AND () OR (||) and NOT (!)*/

let dAsh = "good" || "bad";
console.log(dAsh);

let gAsh = "true" || "false";
console.log(gAsh);

console.log(5 || 8);

console.log(false || false);

console.log(!true);
console.log(!false);
/* if one of this is true the result will be true and if all of this is false the result will be false*/
console.log(10 > 12 || 6 > 10);
console.log(10 < 15 || 3 > 5);

